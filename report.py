def generate_styled_table(doc, data_rows, headers, col_widths=None, hi_col=None):
    """Builds a structured, styled data grid matching the original template."""
    table = doc.add_table(rows=len(data_rows) + 1, cols=len(headers))
    table.alignment = WD_TABLE_ALIGNMENT.CENTER
    table.autofit = False
    
    # Format Header Row
    hdr_cells = table.rows[0].cells
    for idx, header_text in enumerate(headers):
        hdr_cells[idx].text = header_text
        set_cell_background(hdr_cells[idx], HEX_NAVY)
        set_cell_margins(hdr_cells[idx], top=100, bottom=100, left=120, right=120)
        p = hdr_cells[idx].paragraphs[0]
        p.alignment = WD_ALIGN_PARAGRAPH.LEFT
        for run in p.runs:
            run.font.bold = True
            run.font.size = Pt(9)
            run.font.color.rgb = RGBColor.from_string("FFFFFF")
            
    # Format Data Rows
    for r_idx, row_data in enumerate(data_rows):
        row_cells = table.rows[r_idx + 1].cells
        bg_color = HEX_GREY_LT if r_idx % 2 == 1 else "FFFFFF"
        
        for c_idx, val in enumerate(row_data):
            row_cells[c_idx].text = str(val)
            set_cell_background(row_cells[c_idx], bg_color)
            set_cell_margins(row_cells[c_idx], top=80, bottom=80, left=120, right=120)
            
            # Apply light gray borders to mimic grid lines
            set_cell_borders(row_cells[c_idx],
                             top={"sz": 4, "val": "single", "color": HEX_LINE},
                             bottom={"sz": 4, "val": "single", "color": HEX_LINE},
                             left={"sz": 4, "val": "single", "color": HEX_LINE},
                             right={"sz": 4, "val": "single", "color": HEX_LINE})
            
            p = row_cells[c_idx].paragraphs[0]
            for run in p.runs:
                run.font.size = Pt(9)
                if hi_col is not None and c_idx == hi_col:
                    run.font.bold = True
                    run.font.color.rgb = RGBColor.from_string(HEX_BLUE)
                else:
                    run.font.color.rgb = RGBColor.from_string(HEX_DARK)
                    
    # Distribute dynamic column width allocations
    if col_widths:
        for row in table.rows:
            for idx, w in enumerate(col_widths):
                row.cells[idx].width = Inches(w)
                
    p_spacer = doc.add_paragraph()
    p_spacer.paragraph_format.space_after = Pt(6)

