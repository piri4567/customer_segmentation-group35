def add_bullet(doc, text):
    """Adds a standard bullet point with controlled text formatting."""
    p = doc.add_paragraph(style='List Bullet')
    p.paragraph_format.space_after = Pt(3)
    p.paragraph_format.line_spacing = 1.15
    # Word interprets HTML tags like <b> manually inside runs:
    parts = text.split("<b>")
    if len(parts) == 1:
        run = p.add_run(text)
        run.font.size = Pt(10)
        run.font.color.rgb = RGBColor.from_string(HEX_DARK)
    else:
        for part in parts:
            if "</b>" in part:
                bold_txt, normal_txt = part.split("</b>")
                r1 = p.add_run(bold_txt)
                r1.bold = True
                r1.font.size = Pt(10)
                r1.font.color.rgb = RGBColor.from_string(HEX_DARK)
                r2 = p.add_run(normal_txt)
                r2.font.size = Pt(10)
                r2.font.color.rgb = RGBColor.from_string(HEX_DARK)
            else:
                r0 = p.add_run(part)
                r0.font.size = Pt(10)
                r0.font.color.rgb = RGBColor.from_string(HEX_DARK)

