def add_image(doc, filename, width_inch=5.5):
    """Loads and appends an image from outputs/. Appends text placeholder if missing."""
    path = os.path.join(OUTPUTS_DIR, filename)
    if not os.path.exists(path):
        p = doc.add_paragraph()
        run = p.add_run(f"[Missing Image Asset: {filename}]")
        run.font.italic = True
        run.font.color.rgb = RGBColor.from_string(HEX_GREY)
        p.alignment = WD_ALIGN_PARAGRAPH.CENTER
        return
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    p.paragraph_format.space_after = Pt(4)
    p.add_run().add_picture(path, width=Inches(width_inch))

