# ── XML Shading Helpers for Native Word Elements ──────────────────────────────

def set_cell_background(cell, fill_hex):
    """Sets background color of a table cell."""
    tc_pr = cell._element.get_or_add_tcPr()
    shd = OxmlElement('w:shd')
    shd.set(qn('w:val'), 'clear')
    shd.set(qn('w:color'), 'auto')
    shd.set(qn('w:fill'), fill_hex)
    tc_pr.append(shd)

def set_cell_margins(cell, top=100, bottom=100, left=150, right=150):
    """Sets internal padding for a table cell."""
    tc_pr = cell._element.get_or_add_tcPr()
    tc_mar = OxmlElement('w:tcMar')
    for m, val in [('w:top', top), ('w:bottom', bottom), ('w:left', left), ('w:right', right)]:
        node = OxmlElement(m)
        node.set(qn('w:w'), str(val))
        node.set(qn('w:type'), 'dxa')
        tc_mar.append(node)
    tc_pr.append(tc_mar)

def set_cell_borders(cell, **kwargs):
    """
    Sets specific cell borders. Usage:
    set_cell_borders(cell, top={"sz": 12, "val": "single", "color": "HEX"})
    """
    tc_pr = cell._element.get_or_add_tcPr()
    tc_borders = OxmlElement('w:tcBorders')
    for border_name, border_props in kwargs.items():
        border = OxmlElement(f'w:{border_name}')
        for key, val in border_props.items():
            border.set(qn(f'w:{key}'), str(val))
        tc_borders.append(border)
    tc_pr.append(tc_borders)

def add_heading_styled(doc, text, level, space_before=12, space_after=6, hex_color=HEX_DARK):
    """Creates a standardized heading with programmatic spacing and color."""
    p = doc.add_heading(text, level=level)
    p.paragraph_format.space_before = Pt(space_before)
    p.paragraph_format.space_after = Pt(space_after)
    p.paragraph_format.keep_with_next = True
    for run in p.runs:
        run.font.color.rgb = RGBColor.from_string(hex_color)
        run.font.name = 'Arial'
    return p

