# ── Custom Flowables ──────────────────────────────────────────────────────────

class ColorBar(Flowable):
    """A full-width solid horizontal bar."""
    def __init__(self, color, height=6):
        super().__init__()
        self.color  = color
        self._h     = height
        self.width  = AVAIL
        self.height = height

    def draw(self):
        self.canv.setFillColor(self.color)
        self.canv.rect(0, 0, self.width, self._h, fill=1, stroke=0)


class SectionHeader(Flowable):
    """Coloured rounded-rect section banner with white text."""
    def __init__(self, text, color=BLUE):
        super().__init__()
        self.text   = text
        self.color  = color
        self.width  = AVAIL
        self.height = 28

    def draw(self):
        c = self.canv
        c.setFillColor(self.color)
        c.roundRect(0, 0, self.width, 28, 4, fill=1, stroke=0)
        c.setFillColor(WHITE)
        c.setFont("Helvetica-Bold", 11.5)
        c.drawString(12, 9, self.text)


class SegmentBadge(Flowable):
    """Coloured left-border segment identity strip."""
    def __init__(self, name, color, size, pct, avg_spend):
        super().__init__()
        self.name      = name
        self.color     = color
        self.size      = size
        self.pct       = pct
        self.avg_spend = avg_spend
        self.width     = AVAIL
        self.height    = 34

    def draw(self):
        c = self.canv
        c.setFillColor(self.color)
        c.rect(0, 0, 4, 34, fill=1, stroke=0)
        c.setFillColor(HexColor("#f8fafc"))
        c.rect(4, 0, self.width - 4, 34, fill=1, stroke=0)
        c.setStrokeColor(HexColor("#e2e8f0"))
        c.rect(4, 0, self.width - 4, 34, fill=0, stroke=1)
        c.setFillColor(self.color)
        c.setFont("Helvetica-Bold", 10.5)
        c.drawString(14, 20, self.name)
        c.setFillColor(HexColor("#64748b"))
        c.setFont("Helvetica", 8)
        c.drawString(14, 7, f"{self.size} customers  |  {self.pct}  |  Avg lifetime spend {self.avg_spend}")


