def img(filename, width=None):
    """Load an image from outputs/. Returns a placeholder paragraph if missing."""
    path = os.path.join(OUTPUTS_DIR, filename)
    if not os.path.exists(path):
        return Paragraph(f"<i>[Missing: {filename}]</i>", caption_s)
    i = Image(path)
    if width:
        ratio        = i.imageHeight / float(i.imageWidth)
        i.drawWidth  = width
        i.drawHeight = width * ratio
    return i


def bullet(text):
    return Paragraph(f"<bullet>&bull;</bullet> {text}", bullet_s)


def kv_table(rows):
    """Two-column label/value info table used on the cover page."""
